	private static void solve() throws IOException {
	    int n = nextInt();
		int[] a = new int[100];
		for (int i = 0; i < n; i++) {
			a[i] = nextInt();
		}
		for (int i = 0; i < n; i++) {
			for (int j = i + 1; j < n; j++) {
				for (int k = j+1; k < n; k++) {
					if (a[i] + a[j] == a[k]) {
						writer.print((k + 1) + " " + (i + 1) + " " + (j + 1));
						return;
					}
					if (a[j] + a[k] == a[i]) {
						writer.print((i + 1) + " " + (j + 1) + " " + (k + 1));
						return;
					}
					if (a[i] + a[k] == a[j]) {
						writer.print((j + 1) + " " + (i + 1) + " " + (k + 1));
						return;
					}
				}
			}
		}
		writer.print(-1);
	}
